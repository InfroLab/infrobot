#Pack model
class Pack:
    def __init__(self, author, author_image, image, thumb, name, link, short_desc, desc):
        self.author = author
        self.author_image = author_image
        self.image = image
        self.thumb = thumb
        self.name = name
        self.link = link
        self.short_desc = short_desc
        self.desc = desc